    <p> 
      Dans ce numéro de la revue Faire, il y est présenté 3 cas de figures différents sur le livre-galerie à travers 3 expositions. Comment lier travaux, édition et espace? Je retiens surtout l'exposition «&#8239Title of the Show&#8239» de Julia Born à la Galerie für Zeitgenössische Kunst de Leipzig. Ici, la designeuse pense sa monographie comme un catalogue d'exposition à l'échelle d'une exposition. Sur chaque mur y est présenté une page de ce livre géant. Numérotés, ils guident le spectateur dans l'espace et permet son adaptation imprimé. Il n'y a aucun travail de mise en page dans l'édition imprimé si ce n'est que l'utilisation des photos de l'exposition qui mettent déjà en forme le travaille de Julia Born.
    </p>
   

