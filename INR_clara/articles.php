    <h3>
      LYOTARD, Jean-François.  
    </h3>

    <h4>
      <a href="https://blogs.esam-c2.fr/design-graphique/intriguer-ou-le-paradoxe-du-graphiste-de-jean-francois-lyotard/" target="_blank">
        Intriguer, ou le paradoxe du graphiste, 
      </a>
    </h4> 
    
    <h5>
      Blogs esam, 2019.
    </h5>
    
    <blockquote cit="https://blogs.esam-c2.fr/design-graphique/intriguer-ou-le-paradoxe-du-graphiste-de-jean-francois-lyotard/">En visant le plaisir, l'obiet appartient à l'esthétique. En visant la croyance, il relève de la rhétorique</blockquote>
    
    <p> 
      D'après Jean-François Lyotard, Le graphisme est un «&#8239art de rue&#8239», un «&#8239art éphémère&#8239», conçu pour des évènements compris dans un temps limité. Le graphiste est concepteur d'«&#8239art rapide&#8239» et ce doit d'intriguer le passant pour l'informer. Il doit donc se démarquer esthétiquement tout en respectant l'objet du commanditaire pour qu'on puisse s'arrêter pour le regarder. Cependant, il n'est pas destiné à être exposé, selon l'auteur, puisque d'une part, il ne saura pas satisfaire la sensibilité du nouveau spectateur, «&#8239cette bête obscure&#8239» et perdra tout sens placé hors de son contexte temporel et géographique. Coincé entre le rôle d'artiste et d'interprête, comment le designer parvient-il à s'émanciper&#8239? 
    </p>
   

